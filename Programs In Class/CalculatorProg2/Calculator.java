    private void equalsKey() {
        String expr = infix.toString();
        double result = (!expr.equals("")) ? Expression.eval(expr) : 0;
        displayLabel.setText("" + result);
        infix = new StringBuilder("" + result);
        nextOperand = true;
        equalsPressed = true;
    }

    private void clearKey() {
        infix = new StringBuilder();
        displayLabel.setText("0");
        nextOperand = true;
    }

    private void pressKey(String key) {
        if (equalsPressed) {
            displayLabel.setText("");
            infix = new StringBuilder();
            equalsPressed = false;
            nextOperand = false;
        } else if (nextOperand) {
            displayLabel.setText("");
            nextOperand = false;
        }
        String prev = displayLabel.getText();
        displayLabel.setText(prev + key);
        infix.append(key);
    }
    
    private void operator(String op) {
        equalsPressed = false;
        infix.append(Expression.SPACE + op + Expression.SPACE);
        nextOperand = true;    
    }
    
